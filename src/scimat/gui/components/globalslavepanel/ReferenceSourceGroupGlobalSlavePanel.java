  @Override
  public void refresh(ReferenceSourceGroup item) {

    setMasterItem(item);

    this.referenceSourceGroupDetailPanel.refreshItem(item);
    this.referenceSourceGroupSlaveReferenceSourcesPanel.setMasterItem(item);

    if (item != null) {
      
      this.updateReferenceSourceGroupButton.setEnabled(true);

    } else {

      this.referenceSourceGroupDetailPanel.refreshItem(null);

    }
  }

