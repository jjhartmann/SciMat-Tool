  private void updateReferenceSourceGroupButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateReferenceSourceGroupButtonActionPerformed
    
    PerformKnowledgeBaseEditTask task;

    // If the name is empty
    if (this.referenceSourceGroupDetailPanel.getGroupName().isEmpty()) {

      JOptionPane.showMessageDialog(this, "You have to give a group name.\nPlease, " +
        "give a name for the group.", "Invalid group name", JOptionPane.ERROR_MESSAGE);

    } else {

      task = new PerformKnowledgeBaseEditTask(new UpdateReferenceSourceGroupEdit(getMasterItem().getReferenceSourceGroupID(),
              this.referenceSourceGroupDetailPanel.getGroupName(),
              this.referenceSourceGroupDetailPanel.isStopGroup()), this);

      task.execute();
    }
  }//GEN-LAST:event_updateReferenceSourceGroupButtonActionPerformed

