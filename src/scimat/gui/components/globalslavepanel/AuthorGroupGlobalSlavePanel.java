  @Override
  public void refresh(AuthorGroup item) {

    setMasterItem(item);

    this.authorGroupDetailPanel.refreshItem(item);
    this.authorGroupSlaveAuthorsPanel.setMasterItem(item);

    if (item != null) {
      
      this.updateAuthorGroupButton.setEnabled(true);

    } else {
      
      this.updateAuthorGroupButton.setEnabled(false);

    }
  }

