  @Override
  public void refresh(WordGroup item) {

    setMasterItem(item);

    this.wordGroupDetailPanel.refreshItem(item);
    this.wordGroupSlaveWordsPanel.setMasterItem(item);

    if (item != null) {
      
      this.updateWordGroupButton.setEnabled(true);

    } else {
      
      this.updateWordGroupButton.setEnabled(false);

    }
  }

