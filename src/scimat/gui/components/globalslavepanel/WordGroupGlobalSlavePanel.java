  private void updateWordGroupButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateWordGroupButtonActionPerformed

    PerformKnowledgeBaseEditTask task;

    // If the name is empty
    if (this.wordGroupDetailPanel.getGroupName().isEmpty()) {

      JOptionPane.showMessageDialog(this, "You have to give a group name.\nPlease, " +
        "give a name for the group.", "Invalid group name", JOptionPane.ERROR_MESSAGE);

    } else {

      task = new PerformKnowledgeBaseEditTask(new UpdateWordGroupEdit(getMasterItem().getWordGroupID(),
              this.wordGroupDetailPanel.getGroupName(),
              this.wordGroupDetailPanel.isStopGroup()), this);

      task.execute();
    }
  }//GEN-LAST:event_updateWordGroupButtonActionPerformed

