  private void updateAuthorReferenceGroupButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateAuthorReferenceGroupButtonActionPerformed

    PerformKnowledgeBaseEditTask task;

    // If the name is empty
    if (this.authorReferenceGroupDetailPanel.getGroupName().isEmpty()) {

      JOptionPane.showMessageDialog(this, "You have to give a group name.\nPlease, " +
        "give a name for the group.", "Invalid group name", JOptionPane.ERROR_MESSAGE);

    } else {

      task = new PerformKnowledgeBaseEditTask(new UpdateAuthorReferenceGroupEdit(getMasterItem().getAuthorReferenceGroupID(),
              this.authorReferenceGroupDetailPanel.getGroupName(),
              this.authorReferenceGroupDetailPanel.isStopGroup()), this);

      task.execute();
    }
  }//GEN-LAST:event_updateAuthorReferenceGroupButtonActionPerformed

