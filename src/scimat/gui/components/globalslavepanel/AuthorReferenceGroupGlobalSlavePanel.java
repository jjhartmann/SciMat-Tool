  public void refresh(AuthorReferenceGroup item) {

    setMasterItem(item);

    this.authorReferenceGroupDetailPanel.refreshItem(item);
    this.authorReferenceGroupSlaveAuthorReferencesPanel.setMasterItem(item);

    if (item != null) {
      
      this.updateAuthorReferenceGroupButton.setEnabled(true);

    } else {

      this.updateAuthorReferenceGroupButton.setEnabled(false);

    }
  }

