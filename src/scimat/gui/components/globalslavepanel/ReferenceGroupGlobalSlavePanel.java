  @Override
  public void refresh(ReferenceGroup item) {

    setMasterItem(item);

    this.referenceGroupDetailPanel.refreshItem(item);
    this.referenceGroupSlaveReferencesPanel.setMasterItem(item);

    if (item != null) {
      
      this.updateReferenceGroupButton.setEnabled(true);

    } else {
      
      this.updateReferenceGroupButton.setEnabled(false);

    }
  }

