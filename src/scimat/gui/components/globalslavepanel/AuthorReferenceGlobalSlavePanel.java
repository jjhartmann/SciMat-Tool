  @Override
  public void refresh(AuthorReference item) {

    setMasterItem(item);

    this.authorReferenceDetailPanel.refreshItem(item);
    this.authorReferenceSlaveAuthorPanel.setMasterItem(item);
    this.authorReferenceSlaveAuthorReferenceGroupPanel.setMasterItem(item);
    this.authorReferenceSlaveReferencesPanel.setMasterItem(item);

    if (item != null) {
      
      this.updateAuthorReferenceButton.setEnabled(true);

    } else {
      
      this.updateAuthorReferenceButton.setEnabled(false);
    }
  }

