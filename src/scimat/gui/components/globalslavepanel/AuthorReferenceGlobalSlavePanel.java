  private void updateAuthorReferenceButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateAuthorReferenceButtonActionPerformed

    PerformKnowledgeBaseEditTask task;

    // If the name is empty
    if (this.authorReferenceDetailPanel.getAuthorName().isEmpty()) {

      JOptionPane.showMessageDialog(this, "You have to give a name.\nPlease, " +
        "give a name for the author reference.", "Invalid author reference name", JOptionPane.ERROR_MESSAGE);

    } else {

      task = new PerformKnowledgeBaseEditTask(new UpdateAuthorReferenceEdit(getMasterItem().getAuthorReferenceID(),
              this.authorReferenceDetailPanel.getAuthorName()), this);

      task.execute();
    }
  }//GEN-LAST:event_updateAuthorReferenceButtonActionPerformed

