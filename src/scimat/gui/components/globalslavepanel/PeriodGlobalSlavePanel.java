  /**
   * 
   * @param evt 
   */
  private void addPublishDateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addPublishDateButtonActionPerformed
   
    
    try {
      
      PeriodDAO periodDAO = CurrentProject.getInstance().getFactoryDAO().getPeriodDAO();
      PublishDateDAO publishDateDAO = CurrentProject.getInstance().getFactoryDAO().getPublishDateDAO();
      
      ArrayList<PublishDate> publishDatesInDocument = periodDAO.getPublishDates(getMasterItem().getPeriodID());
      TreeSet<PublishDate> possiblePublishDates = new TreeSet<PublishDate>(publishDateDAO.getPublishDates());
      
      possiblePublishDates.removeAll(publishDatesInDocument);
      
      AddDialogManager.getInstance().showAddPublishDatesToPeriodDialog(getMasterItem(), new ArrayList<PublishDate>(possiblePublishDates));
      
    } catch (KnowledgeBaseException e) {
    
      ErrorDialogManager.getInstance().showException(e);
    }
  }//GEN-LAST:event_addPublishDateButtonActionPerformed

