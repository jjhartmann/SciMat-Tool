  private void updatePeriodButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatePeriodButtonActionPerformed

    PerformKnowledgeBaseEditTask task;

    // If the name is empty
    if (this.periodDetailPanel.getPeriodName().isEmpty()) {

      JOptionPane.showMessageDialog(this, "You have to give a name.\nPlease, " +
        "give a name for the period.", "Invalid period name", JOptionPane.ERROR_MESSAGE);

    } else {

      task = new PerformKnowledgeBaseEditTask(new UpdatePeriodEdit(getMasterItem().getPeriodID(),
              this.periodDetailPanel.getPeriodName(), this.periodDetailPanel.getPosition()), this);

      task.execute();
    }
  }//GEN-LAST:event_updatePeriodButtonActionPerformed

