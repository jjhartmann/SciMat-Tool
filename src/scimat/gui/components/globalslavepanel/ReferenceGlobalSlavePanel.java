  /**
   * 
   */
  private void addAuthorReferenceButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addAuthorReferenceButtonActionPerformed
    
    try {
      
      ReferenceDAO referenceDAO = CurrentProject.getInstance().getFactoryDAO().getReferenceDAO();
      AuthorReferenceDAO authorReferenceDAO = CurrentProject.getInstance().getFactoryDAO().getAuthorReferenceDAO();
      
      ArrayList<AuthorReference> authorsInDocument = referenceDAO.getAuthorReferences(getMasterItem().getReferenceID());
      TreeSet<AuthorReference> possibleAuthors = new TreeSet<AuthorReference>(authorReferenceDAO.getAuthorReferences());
      
      possibleAuthors.removeAll(authorsInDocument);
      
      AddDialogManager.getInstance().showAddAuthorReferencesToReferenceDialog(getMasterItem(), new ArrayList<AuthorReference>(possibleAuthors));
      
    } catch (KnowledgeBaseException e) {
    
      ErrorDialogManager.getInstance().showException(e);
    }
}//GEN-LAST:event_addAuthorReferenceButtonActionPerformed

  
