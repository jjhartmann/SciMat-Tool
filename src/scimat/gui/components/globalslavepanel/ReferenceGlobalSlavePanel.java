  private void updateReferenceButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateReferenceButtonActionPerformed
    
    PerformKnowledgeBaseEditTask task;

    // If the name is empty
    if (this.referenceDetailPanel.getFullReference().isEmpty()) {

      JOptionPane.showMessageDialog(this, "You have to give a full reference.\nPlease, " +
        "give a full reference for the reference.", "Invalid full reference", JOptionPane.ERROR_MESSAGE);

    } else {

      task = new PerformKnowledgeBaseEditTask(new UpdateReferenceEdit(getMasterItem().getReferenceID(),
              this.referenceDetailPanel.getFullReference(),
              this.referenceDetailPanel.getVolume(),
              this.referenceDetailPanel.getIssue(),
              this.referenceDetailPanel.getPage(),
              this.referenceDetailPanel.getYear(),
              this.referenceDetailPanel.getDoi(),
              this.referenceDetailPanel.getFormat()), this);

      task.execute();
    }
  }//GEN-LAST:event_updateReferenceButtonActionPerformed

  /**
   * 
   * @param evt 
   */
  private void addReferenceSourceButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addReferenceSourceButtonActionPerformed
    
    
    try {
      
      ReferenceDAO referenceDAO = CurrentProject.getInstance().getFactoryDAO().getReferenceDAO();
      ReferenceSourceDAO referenceSourceDAO = CurrentProject.getInstance().getFactoryDAO().getReferenceSourceDAO();
      
      ReferenceSource referenceSource = referenceDAO.getReferenceSource(getMasterItem().getReferenceID());
      TreeSet<ReferenceSource> referenceSources = new TreeSet<ReferenceSource>(referenceSourceDAO.getReferenceSources());
      
      if (referenceSource != null) {
        
        referenceSources.remove(referenceSource);
      }
      
      AddDialogManager.getInstance().showAddReferenceSourcesToReferenceDialog(getMasterItem(), new ArrayList<ReferenceSource>(referenceSources));
      
    } catch (KnowledgeBaseException e) {
    
      ErrorDialogManager.getInstance().showException(e);
    }
}//GEN-LAST:event_addReferenceSourceButtonActionPerformed

