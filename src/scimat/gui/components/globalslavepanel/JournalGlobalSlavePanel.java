  private void updateJournalButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateJournalButtonActionPerformed

    PerformKnowledgeBaseEditTask task;

    // If the name is empty
    if (this.journalDetailPanel.getSource().isEmpty()) {

      JOptionPane.showMessageDialog(this, "You have to give a source.\nPlease, " +
        "give a source for the journal.", "Invalid journal source", JOptionPane.ERROR_MESSAGE);

    } else {

      task = new PerformKnowledgeBaseEditTask(new UpdateJournalEdit(getMasterItem().getJournalID(),
              this.journalDetailPanel.getSource(),
              this.journalDetailPanel.getConferenceInformation()), this);

      task.execute();
    }
  }//GEN-LAST:event_updateJournalButtonActionPerformed

