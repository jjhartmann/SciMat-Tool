  @Override
  public void refresh(Journal item) {

    setMasterItem(item);

    this.journalDetailPanel.refreshItem(item);
    this.journalSlaveDocumentsPanel.setMasterItem(item);
    this.journalSlaveJournalSubjectCategoryPublishDatePanel.setMasterItem(item);

    if (item != null) {
      
      this.updateJournalButton.setEnabled(true);

    } else {
      
      this.updateJournalButton.setEnabled(false);

    }
  }

