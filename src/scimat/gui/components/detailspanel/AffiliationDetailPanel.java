  public void reset() {

    idTextField.setText("");
    fullAffiliationTextField.setText("");

    setEnableTextField(false);
  }

  /**
   *
   */
  public void setEnableTextField(boolean enabled) {

    idTextField.setEnabled(enabled);
    fullAffiliationTextField.setEnabled(enabled);
  }

  /**
   * 
   * @param affiliation
   */
  public void refreshItem(Affiliation affiliation) {

    this.affiliation = affiliation;

    if (this.affiliation != null) {

      idTextField.setText(this.affiliation.getAffiliationID().toString());
      fullAffiliationTextField.setText(this.affiliation.getFullAffiliation());
      
      setEnableTextField(true);

    } else {

      reset();

    }
  }

  /**
   * 
   * @return
   */
  public String getFullAffiliation() {

    return fullAffiliationTextField.getText();
  }

