  /**
   * 
   * @param items
   * @throws KnowledgeBaseException
   */
  public void entityUpdated(ArrayList<Affiliation> items) throws KnowledgeBaseException {

    int position;

    position = items.indexOf(this.affiliation);

    if (position != -1) {

      refreshItem(items.get(position));

    }
  }

